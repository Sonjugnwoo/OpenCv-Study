#include "ImagePreprocessor.h"
// ====================================================================================
// 전처리 
// ====================================================================================
cv::Mat ImagePreprocessor::preprocessor_image(const cv::Mat& input) {
	cv::Mat result;
	// ════════════════════════════════════════════
	// GaussianBlur - 노이즈 제거
	// ════════════════════════════════════════════
	// Size(5,5), sigmaX/Y=1.5: 로고 디테일 보존하면서 부드럽게
	cv::GaussianBlur(input, result, cv::Size(5, 5), 1.5, 1.5);  // 결함/배경 분리

	// ════════════════════════════════════════════
	// BGR → 그레이스케일 - 채널 통일
	// ════════════════════════════════════════════
	// 템플릿 매칭 필수: 3채널→1채널 (색상 영향 제거)
	cv::Mat gray;
	cv::cvtColor(result, gray, cv::COLOR_BGR2GRAY);  // 로고 검출 집중

	return result;
}